print(5) # вывести тип число
print(type(5)) # тип данных
print(5 + 5) # сумма чисел
print(5 - 5) # разность чисел
print(5 * 5) # произведение чисел
print(5 / 5) # деление чисел
print(5 // 5) # целоцичсленное деление
print(5 % 5) # остаток от деления
print(5 % 2) # остаток от деления на 2 - проверка на чётность
print (5 ** 2) # возведение числа в степень
print (2.0) # число с плавающей точкой
print ('Hello, world') # тип данных текст  - вывоводится через кавычки
